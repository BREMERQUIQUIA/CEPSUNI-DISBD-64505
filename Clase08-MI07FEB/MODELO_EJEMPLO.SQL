CREATE TABLE CLIENTE
( 
	IDCLIENTE            integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(100)  NOT NULL ,
	DIRECCION            varchar(200)  NOT NULL ,
	CELULAR              varchar(20)  NOT NULL ,
	CONSTRAINT XPKCLIENTE PRIMARY KEY (IDCLIENTE ASC)
)
go



CREATE TABLE VENTA
( 
	IDVENTA              integer IDENTITY ( 1,1 ) ,
	IDCLIENTE            integer  NOT NULL ,
	FECHA                datetime  NOT NULL ,
	TOTAL                money  NOT NULL ,
	CONSTRAINT XPKVENTA PRIMARY KEY (IDVENTA ASC),
	CONSTRAINT FK_VENTA_CLIENTE FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE(IDCLIENTE)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE PRODUCTO
( 
	IDPRODUCTO           integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(100)  NOT NULL ,
	PRECIO               money  NOT NULL ,
	STOCK                integer  NOT NULL ,
	CONSTRAINT XPKPRODUCTO PRIMARY KEY (IDPRODUCTO ASC)
)
go



CREATE TABLE ITEMS
( 
	IDPRODUCTO           integer  NOT NULL ,
	IDVENTA              integer  NOT NULL ,
	CANTIDAD             integer  NOT NULL ,
	PRECIO               money  NOT NULL ,
	SUBTOTAL             money  NOT NULL ,
	CONSTRAINT XPKITEMS PRIMARY KEY (IDPRODUCTO ASC,IDVENTA ASC),
	CONSTRAINT FK_ITEMS_PRODUCTO FOREIGN KEY (IDPRODUCTO) REFERENCES PRODUCTO(IDPRODUCTO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_ITEMS_VENTA FOREIGN KEY (IDVENTA) REFERENCES VENTA(IDVENTA)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


